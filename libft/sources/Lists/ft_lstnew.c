#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*element;

	element = malloc(sizeof(t_list));
	if (!element)
		return (NULL);
	else if (element)
	{
		element->content = content;
		element->next = NULL;
	}
	return (element);
}
