#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;

	if (!s || !f)
		return ;
	i = 0;
	if (s[i])
	{
		while (s[i])
		{
			(*f)(i, s + i);
			i++;
		}
	}
}
